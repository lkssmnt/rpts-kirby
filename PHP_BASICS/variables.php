<?php

/*
  Variablen werden bei PHP immer mit einem $ gekennzeichnet. Überall wo ihr diese Variable benutzt,
  muss das Dollarzeichen deshalb vor dem Namen stehen.

  Variablen können verschiedene Typen haben:
*/

## Integer sind ganze Zahlen
$myInt = 1;

## Floats sind Zahlen mit möglichen Kommastellen
$myFloat = 0.0001;

## Strings sind Zeichenketten
$myString = "Hello World!";

## Mehrere Strings können wir mit einem "." aneinanderfügen.
$myCombinedString = $myString . " How are you?"; // "Hello World! How are you?"

## Booleans können nur true oder false sein. Bei PHP werden sie normalerweise als 1 oder 0 ausgegeben.
## 1 steht hierbei für true und 0 für false
$myBoolean = false;
